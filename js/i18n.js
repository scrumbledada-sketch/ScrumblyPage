const LANG_STORAGE_KEY = "scrumble-site-lang";
const DEFAULT_LANG = "pt";

function getStoredLang() {
  const stored = localStorage.getItem(LANG_STORAGE_KEY);
  if (stored && I18N[stored]) return stored;
  const browser = (navigator.language || "pt").slice(0, 2).toLowerCase();
  if (I18N[browser]) return browser;
  return DEFAULT_LANG;
}

function setLang(lang) {
  if (!I18N[lang]) return;
  localStorage.setItem(LANG_STORAGE_KEY, lang);
  document.documentElement.lang = lang === "pt" ? "pt-BR" : lang;
  applyTranslations(lang);
  document.querySelectorAll(".lang-btn").forEach((btn) => {
    btn.classList.toggle("is-active", btn.dataset.lang === lang);
    btn.setAttribute("aria-pressed", btn.dataset.lang === lang ? "true" : "false");
  });
}

function applyTranslations(lang) {
  const strings = I18N[lang];
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    const value = strings[key];
    if (value == null) return;
    if (el.tagName === "META") {
      el.setAttribute("content", value);
    } else {
      el.textContent = value;
    }
  });
  document.querySelectorAll("[data-i18n-aria]").forEach((el) => {
    const key = el.getAttribute("data-i18n-aria");
    const value = strings[key];
    if (value != null) el.setAttribute("aria-label", value);
  });
  const titleEl = document.querySelector("title[data-i18n]");
  if (titleEl && strings["meta.title"]) {
    document.title = strings["meta.title"];
  }
  const metaDesc = document.querySelector('meta[name="description"]');
  if (metaDesc && strings["meta.description"]) {
    metaDesc.setAttribute("content", strings["meta.description"]);
  }
  document.dispatchEvent(new CustomEvent("scrumble:langchange", { detail: { lang } }));
}

function initI18n() {
  const lang = getStoredLang();
  setLang(lang);
  document.querySelectorAll(".lang-btn").forEach((btn) => {
    btn.addEventListener("click", () => setLang(btn.dataset.lang));
  });
}
